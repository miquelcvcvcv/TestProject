package de.arvato.geo.domain;

public class Stringfloat {
	String s;
	float i;

	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public float getI() {
		return i;
	}
	public void setI(float i) {
		this.i = i;
	}
	public Stringfloat() {
		s=new String();
		i=0;
	}
 
}
