package de.arvato.geo.domain;



public class Boolfloat {
	private float i;
	 private boolean b;
	 
 public float getI() {
		return i;
	}
	public void setI(float i) {
		this.i = i;
	}
	public boolean isB() {
		return b;
	}
	public void setB(boolean b) {
		this.b = b;
	}

	public Boolfloat() {
		i=0;
		b=false;
	}
 
	
	
}