package de.arvato.geo.domain;

public class Boolint {
	private int i;
	 private boolean b;
	 
 public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public boolean isB() {
		return b;
	}
	public void setB(boolean b) {
		this.b = b;
	}

	public Boolint() {
		i=0;
		b=false;
	}
 
	
	
}
